                <p>
                    Normal photography, also known as standard photography, refers to the practice of capturing images using traditional photographic techniques, without any digital manipulation or post-processing. This type of photography involves using a camera to capture light onto a photosensitive material, such as film or a photographic paper, to produce a physical print of the image.
                    <br>
                    In contrast to digital photography, which allows for instant review and manipulation of images, normal photography requires careful consideration of various factors such as lighting, shutter speed, aperture, and film type to achieve the desired result. Normal photography also requires the use of specialized equipment, such as film cameras, lenses, and darkroom equipment.
